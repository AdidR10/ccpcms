const mongoose = require('mongoose');

const ContestSchema = new mongoose.Schema({
  link: {
    type: String,
    required: true,
  },
  name: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
  time: {
    type: String,
    required: true,
  },
  duration: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: false,
  },
  type: {
    type: String,
    enum: ['team', 'individual'],
    required: true,
  },
});

module.exports = mongoose.model('Contest', ContestSchema);